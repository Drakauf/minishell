#include "./includes/libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = malloc(sizeof(*str) * (size + 1))))
		return (0);
	ft_bzero(str, size);
	str[size] = '\0';
	return (str);
}
